package com.lcm.utils.request;

public class WxUserInfoBean {
	private String nickName;
	private String cover;
	private String phone;
	private String openId;
	private String refereeOpenId;
	private String byqcode;
	
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getCover() {
		return cover;
	}
	public void setCover(String cover) {
		this.cover = cover;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getOpenId() {
		return openId;
	}
	public void setOpenId(String openId) {
		this.openId = openId;
	}
	public String getRefereeOpenId() {
		return refereeOpenId;
	}
	public void setRefereeOpenId(String refereeOpenId) {
		this.refereeOpenId = refereeOpenId;
	}
	public String getByqcode() {
		return byqcode;
	}
	public void setByqcode(String byqcode) {
		this.byqcode = byqcode;
	}
	
	
}
